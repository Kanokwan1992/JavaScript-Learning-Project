let months = 5;
let month;
switch (months) {
  case 1:
    month = 'January';

    break;

  case 2:
    month = 'February';

    break;

  case 3:
    month = 'March';

    break;

  case 4:
    month = 'April';

    break;

  case 5:
    month = 'May';

    break;

  case 6:
    month = 'June';

    break;

  default:
    month = 'Error';
}
console.log(month);
